#área y para calcular el volumen utiliza el operador de exponente.

var=int(input("Introduce la medida de uno de los lados del cubo: "))
print("El área del cubo, según el valor introducido, es: ",6*var**2)
print("El volumen del cubo, según el valor introducido, es: ",var**3)
